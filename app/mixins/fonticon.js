module.exports = {
	computed: {
		ion_ios_arrow_dropright() {
			return String.fromCharCode(0xf114)
		},
		ion_md_arrow_dropright() {
			return String.fromCharCode(0xf284)
		},
		ion_md_arrow_forward() {
			return String.fromCharCode(0xf287)
		},

		ion_ios_arrow_forward() {
			return String.fromCharCode(0xf3d1)
		}									
	}
}
